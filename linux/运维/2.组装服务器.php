具体执行流程：
	评估访问量，公司对服务器的需求，根据需求，确定服务器要求
	根据服务器特点选择具体硬件，比如服务器需要冗余，需要特别稳定
	搜索比较性能
	通过查询IDC(Internet Data Center)机房，得到不同价格
	知名IDC机房，BGP多路线
硬件：	华硕主板 Z8NA-D6C
		服务器用的RDIMM(registerd dual in-line memeory module)内存，自己电脑用的UDIMM(unbuffered dual in-line memeory module)内存条
		SAS(Serial Attached SCSI)盘(串行连接SCSI接口)，服务器用
		SATA个人电脑用的，接口类型分为2.0,3.0
		内存条：DDR3,DDR4

		CPU内存控制器需要与内存条的频率一样

